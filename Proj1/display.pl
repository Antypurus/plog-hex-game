printAllBoard(NB):-
	initialState(B),
	printRealBoard(B,NB).

printRealBoard(B, NB) :-
  printBoard(B, NB, 0).

printBoard([H|T],[],_) :-
  printLine(H,[],_),
  nl,
  printBoard(T,[],_).
printBoard([],_,_).
printBoard(_,[],_).
printBoard([],[],_).

printBoard([H|T],[H1|T1], N) :-
  N1 is N + 1,
  N2 is N1 mod 3,
  N2 is 0, !,
	printLine(H, H1, N2),
	nl,
	printBoard(T, T1, N1).

printBoard([H|T],[H1|T1], N) :-
  N1 is N + 1,
  N2 is N1 mod 3,
	printLine(H, H1, N2),
	nl,
	printBoard(T, [H1|T1], N1).

printLine([],[],_).
printLine([],[_|[]],_).
printLine([],[_|_],_).
printLine([H|T], [H1|T1], 0) :-
  H is 4,
  !,
  symbol(H1, S),
  write(S),
  printLine(T, T1, _).

printLine([H|T], Board, _) :-
	symbol(H,S),
	write(S),
	printLine(T, Board, _).

printLine([H|T], [], _) :-
  	symbol(H,S),
  	write(S),
  	printLine(T, [], _).
